public class animal 
{
    private String type;
    private String sex;
    private int age;
    
    public void setType(String newType)
    {
        type = newType;
    }
    
     public void setSex(String newSex)
    {
        sex = newSex;
    }
     
      public void setAge(int newType)
    {
        age = newType;
    }
      
    public String getType()
    {
        return type;
    }
    
    public String getSex()
    {
        return sex;
    }
    
    public int getAge()
    {
        return age;
    }
}
